#include "libft.h"

static int		ft_countword(char const *s, char c)
{
	unsigned int i;
	int counter;

	i = 0;
	counter = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != '\0')
			counter++;
		while (s[i] && (s[i] != c))
			i++;
	}
	return (counter);
}


static char		*ft_strndup(char const *s, size_t n)
{
	char	*str;

	str = (char *)malloc(sizeof(char) * n + 1);;
	str = ft_strncpy(str, s, n);
	str[n] = '\0';
	return (str);
}


char	**ft_strsplit(char const *s, char c)
{
	int		i;
	int		j;
	int		k;
	char	**tab;

	i = 0;
	k = 0;
	tab = (char **)malloc(sizeof(char *) * (ft_countword(s, c)) + 1);
	if (!tab)
		return (NULL);
	if (!s || !c)
		return (NULL);
	while (s[i])
	{
		while (s[i] == c)
			i++;
		j = i;
		while (s[i] && s[i] != c)
			i++;
		if (i > j)
		{
			tab[k] = ft_strndup(s + j, i - j);
			k++;
		}
	}
	tab[k] = NULL;
	return (tab);
}
